def parse_matrix_str(matrix_str: str) -> Tuple[List[str], Dict[str, dict]]:
    """
    Parse a provided BLOSUM or PAM matrix into a dictionary
    :param matrix_str: multiple lined string representing the array
    :return: list of amino acid proteins and a dictionary of dictionaries giving lookups for matrix entities
    """
    headers = []
    matrix = dict()
    for line in matrix_str.splitlines(keepends=False):
        if line.startswith('#'):
            continue
        if line.startswith(' '):
            headers = line.strip().upper().split()
            continue
        parts = line.split()
        matrix[parts[0].upper()] = {h: int(v) for h, v in zip(headers, parts[1:])}

    return headers, matrix


class AminoAcidNotFoundError(KeyError):
    pass


def matrix_score(sequence1: str, sequence2: str, matrix_str: str = BLOSUM62) -> int:
    """
    Receives two proteins sequences and a matrix table
    Returns the score of two proteins according to the supplied matrix table
    """
    headers, match_array = parse_matrix_str(matrix_str)
    seq_s = list(zip(sequence1.upper(), sequence2.upper()))

    # test package is expecting a particular format for the error, so passing on the KeyError
    # from the sum is insufficient!
    for ch1, ch2 in seq_s:
        if ch1 not in headers or ch2 not in headers:
            raise AminoAcidNotFoundError(f"Scoring matrix does not support scoring for: ('{ch1}', '{ch2}')")

    score = sum(match_array[ch1][ch2] for ch1, ch2 in seq_s)

    return score


def closest_match(
        reference_sequence: str, query_sequences: List[str], matrix_str: str = BLOSUM62
) -> Union[str, List, None]:
    """
    Receives a reference sequence, a list of query sequences and a matrix table
    Returns the closest matching sequence(s) or None
    """
    if not len(query_sequences):
        return None

    score_list = []
    for query in query_sequences:
        score_list.append((matrix_score(reference_sequence, query, matrix_str), query))

    score_list = sorted(score_list)
    best = score_list.pop()
    result = [best[1]]
    # work down to see if there are any more with the same score...
    while score_list[-1][0] == best[0]:
        result.append(score_list.pop()[1])

    return result if len(result) > 1 else result[0]
